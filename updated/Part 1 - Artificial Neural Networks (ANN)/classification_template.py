"""

Las principales actualizaciones que debemos realizar incluyen:

Importar train_test_split desde model_selection: En versiones recientes de scikit-learn, la función train_test_split se encuentra en sklearn.model_selection, no en sklearn.cross_validation.
Actualizar Imputer: El clasificador Imputer se ha descontinuado y ahora se usa SimpleImputer de sklearn.impute.
Corrección de la Importación de OneHotEncoder y LabelEncoder: En lugar de categorical_features, ahora usaremos la función ColumnTransformer para las transformaciones categóricas.
Uso de SVC en lugar de otro clasificador: Asegúrate de que el clasificador esté correctamente configurado.


Cambios importantes:
train_test_split:
Ahora se importa desde sklearn.model_selection en lugar de sklearn.cross_validation.
SVC para clasificación:
El clasificador SVM sigue siendo utilizado con el kernel lineal, pero puedes cambiar el kernel según tus necesidades (por ejemplo, 'rbf' o 'poly').
Visualización:
Se mantiene la visualización de los resultados con mapas de contorno para mostrar cómo el clasificador divide el espacio de características.
"""

# Importing the libraries
import numpy as np
import matplotlib.pyplot as plt
import pandas as pd

# Importing the dataset
dataset = pd.read_csv('Social_Network_Ads.csv')
X = dataset.iloc[:, [2, 3]].values
y = dataset.iloc[:, 4].values

# Splitting the dataset into the Training set and Test set
from sklearn.model_selection import train_test_split  # Actualizado
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size = 0.25, random_state = 0)

# Feature Scaling
from sklearn.preprocessing import StandardScaler
sc = StandardScaler()
X_train = sc.fit_transform(X_train)
X_test = sc.transform(X_test)

# Fitting classifier to the Training set
from sklearn.svm import SVC  # SVM classifier
classifier = SVC(kernel = 'linear', random_state = 0)  # Kernel lineal
classifier.fit(X_train, y_train)

# Predicting the Test set results
y_pred = classifier.predict(X_test)

# Making the Confusion Matrix
from sklearn.metrics import confusion_matrix
cm = confusion_matrix(y_test, y_pred)

# Visualising the Training set results
from matplotlib.colors import ListedColormap
X_set, y_set = X_train, y_train
X1, X2 = np.meshgrid(np.arange(start = X_set[:, 0].min() - 1, stop = X_set[:, 0].max() + 1, step = 0.01),
                     np.arange(start = X_set[:, 1].min() - 1, stop = X_set[:, 1].max() + 1, step = 0.01))
plt.contourf(X1, X2, classifier.predict(np.array([X1.ravel(), X2.ravel()]).T).reshape(X1.shape),
             alpha = 0.75, cmap = ListedColormap(('red', 'green')))
plt.xlim(X1.min(), X1.max())
plt.ylim(X2.min(), X2.max())
for i, j in enumerate(np.unique(y_set)):
    plt.scatter(X_set[y_set == j, 0], X_set[y_set == j, 1],
                c = ListedColormap(('red', 'green'))(i), label = j)
plt.title('Clasificador (Conjunto de Entrenamiento)')
plt.xlabel('Edad')
plt.ylabel('Salario Estimado')
plt.legend()
plt.show()

# Visualising the Test set results
X_set, y_set = X_test, y_test
